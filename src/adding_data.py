# Save winning locations to CSV#
def save_winning_locations():
    with open('winning_locations.csv', 'w', newline='') as f:
        writer = csv.writer(f)
        # Write header
        writer.writerow(['location_name', 'description',
                         'tile1_row', 'tile1_col', 'tile2_row', 'tile2_col',
                         'tile3_row', 'tile3_col', 'tile4_row', 'tile4_col',
                         'tile5_row', 'tile5_col', 'tile6_row', 'tile6_col',
                         'tile7_row', 'tile7_col', 'tile8_row', 'tile8_col',
                         'empty_row', 'empty_col'])

        for loc in winning_locations:
            row = [loc['name'], loc['description']]
            # Add coordinates for tiles 1-8
            for tile in range(1, 9):
                row.extend(loc[str(tile)])
            # Add empty tile (0)
            row.extend(loc["0"])
            writer.writerow(row)

# Function to save variations for a location
def save_variations_csv(location_name, variations_list, filename):
    with open(filename, 'w', newline='') as f:
        writer = csv.writer(f)
        # Write header with puzzle information
        writer.writerow(['puzzle_id', 'difficulty', 'estimated_moves',
                         'tile1_row', 'tile1_col', 'tile2_row', 'tile2_col',
                         'tile3_row', 'tile3_col', 'tile4_row', 'tile4_col',
                         'tile5_row', 'tile5_col', 'tile6_row', 'tile6_col',
                         'tile7_row', 'tile7_col', 'tile8_row', 'tile8_col',
                         'empty_row', 'empty_col'])

        for i, variation in enumerate(variations_list):
            # Determine difficulty and moves
            if i < 5:
                difficulty = 'Easy'
                moves = 4 + (i % 2)  # 4 or 5 moves
            elif i < 10:
                difficulty = 'Normal'
                moves = 8 + (i % 2)  # 8 or 9 moves
            elif i < 15:
                difficulty = 'Medium'
                moves = 12 + (i % 2)  # 12 or 13 moves
            else:
                difficulty = 'Hard'
                moves = 16 + (i % 2)  # 16 or 17 moves

            puzzle_id = f"{location_name}_puzzle_{i + 1:02d}"

            # Create row
            row = [puzzle_id, difficulty, moves]

            # Add tile coordinates in order (1-8)
            for tile in range(1, 9):
                row.extend(variation[str(tile)])

            # Add empty tile (0)
            row.extend(variation["0"])

            writer.writerow(row)

    print(f"âœ“ Saved {filename}")

